                   <h1> Ganja </h1>
                
                <p>
                    Marijuana adalah kata yang digunakan untuk menggambarkan bunga, batang, biji dan daun kering dari tanaman ganja, Cannabis sativa, tanaman yang mengandung zat pengubah akal sehat delta-9 tetrahydrocannabiol (THC) dan senyawa lain yang terkait.
                </p>
                <p>
                    Orang-orang menggunakan ganja kering/marijuana dengan memasukannya ke dalam lintingan rokok atau ke dalam pipa (bong). Mereka juga terkadang mengosongkan tembakau pada rokok dan diisi dengan marijuana. Guna menghindari asap yang dihasilkan, banyak orang yang menggunakan alat penguap (vaporizer) yang juga sering disebut bong. Alat ini dapat menarik zat-zat aktif, termasuk THC dari ganja dan mengumpulkan uap di unit penyimpanan. 
                </p>
                <p>
                    Efek yang disebabkan ganja cukup banyak seperti :
                </p>
                <ul>
                    <li>
                        Perubahan kesadaran terhadap waktu
                    </li>
                    <li>
                        Perubahan suasana hati  
                    </li>
                    <li>
                        Gerakan tubuh terganggu
                    </li>
                    <li>
                        Kesulitan berpikir dan memecahkan masalah
                    </li>
                    <li>
                        Gangguan terhadap daya ingat
                    </li>
                    <li>
                        Gangguan pernapasan. Asap ganja dapat menyebabkan iritasi pada paru-paru yang memicu batuk berdahak, sakit paru-paru hingga infeksi paru-paru.
                    </li>
                    <li>
                        Meningkatkan denyut jantung. Marijuana dapat meningkatkan denyut jantung setelah 3 jam merokok. Hal ini dapat menyebabkan serangan jantung.
                    </li>
                    <li>
                        Gangguan pada bayi. Penggunaan ganja semasa kehamilan dapat memengaruhi otak dan perilaku pada bayi
                    </li>
                    <li>
                       Halusinasi, paranoia dan berpikir secara tidak teratur. 
                    </li>
                </ul>
